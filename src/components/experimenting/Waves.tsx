interface Point {
    x: number;
    y: number;
}

function generateBezierPath(points: Point[]) {
    let path = `M ${points[0].x},${points[0].y}`;
  
    for (let i = 0; i < points.length - 1; i++) {
      let p0 = points[i];
      let p1 = points[i + 1];
      let cp1x, cp1y, cp2x, cp2y;
  
      if (i === 0) {
        // First control point is the point itself for the first segment
        cp1x = p0.x;
        cp1y = p0.y;
      } else {
        // Average with the previous point to smooth the transition
        cp1x = (p0.x + points[i - 1].x) / 2;
        cp1y = (p0.y + points[i - 1].y) / 2;
      }
  
      if (i === points.length - 2) {
        // Last control point is the end point itself
        cp2x = p1.x;
        cp2y = p1.y;
      } else {
        // Average with the next point to smooth the transition
        cp2x = (p1.x + points[i + 2].x) / 2;
        cp2y = (p1.y + points[i + 2].y) / 2;
      }
  
      path += ` C ${cp1x},${cp1y} ${cp2x},${cp2y} ${p1.x},${p1.y}`;
    }
  
    return path;
  }
  




